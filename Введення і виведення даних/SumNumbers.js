// за допомогою parseFloat
// Запитати у користувача ввести перше число
let firstNumber = parseFloat(prompt("Введіть перше число:"));

// Запитати у користувача ввести друге число
let secondNumber = parseFloat(prompt("Введіть друге число:"));

// Перевірка, чи користувач ввів коректні числа
if (!isNaN(firstNumber) && !isNaN(secondNumber)) {
	// Обчислити суму
	let sum = firstNumber + secondNumber;
	
	// Вивести результат у вказаному форматі
	console.log(`${firstNumber} + ${secondNumber} = ${sum}`);
} else {
	console.log("Будь ласка, введіть коректні числа.");
}
